'''
Tipos de dados bool (boolean)
Ao questionar algo em um programa. só existem duas respostas,
verdadeiro ou falso, sim ou não!
Isso é chamado de dados booleanos.

Podemos usar operados de questionamento, como por exemplo:
> (maior que)
== (questiona a igualdade entre dois valores)
<= (menor ou igual)

E com esses operadores podemos fazer perguntas e começar a desenvolver
de fato nosso código.

'''


print(10 > 20)
print(20 == 20)
print(type(10 > 20))
print(type(20 == 20))