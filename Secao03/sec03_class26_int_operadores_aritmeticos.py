'''Vamos falar sobre Operadores aritméticos em Python'''


# ADIÇÃO

adicao = 10 + 10
print('Adição',type(adicao), adicao)

adicao_02 = 10 + 10.5
print('Adição',type(adicao_02), adicao_02)

adicao_03 = 10.5 + 10.5
print('Adição',type(adicao_03), adicao_03)

# SUBTRAÇÃO

subtracao = 10 - 5
print('Subtração', type(subtracao), subtracao)

subtracao_02 = 10 - 5.5
print('Subtração', type(subtracao_02), subtracao_02)

subtracao_03 = 10.5 - 5.5
print('Subtração', type(subtracao_03), subtracao_03)

# MULTIPLICAÇÃO

multiplicacao = 10 * 10
print('Multiplicação', type(multiplicacao), multiplicacao)

multiplicacao_02 = 10 * 10.5
print('Multiplicação', type(multiplicacao_02), multiplicacao_02)

multiplicacao_03 = 10.5 * 10.3
print('Multiplicação', type(multiplicacao_03), multiplicacao_03)

# DIVISÃO

divisao = 10 / 2
print('Divisão', type(divisao), divisao)

divisao_02 = 10 / 3
print('Divisão', type(divisao_02), divisao_02)

divisao_03 = 10 / 3.5
print('Divisão', type(divisao_03), divisao_03)

# DIVISÃO INTEIRA

divisao_inteira = 10 // 2
print('Divisão Inteira', type(divisao_inteira), divisao_inteira)

divisao_inteira_02 = 10 // 3
print('Divisão Inteira', type(divisao_inteira_02), divisao_inteira_02)

divisao_inteira_03 = 10 // 3.5
print('Divisão Inteira', type(divisao_inteira_03), divisao_inteira_03)


# EXPONENCIAÇÃO

exponencicacao = 2 ** 10
print('Exponenciação', type(exponencicacao), exponencicacao)

exponencicacao_02 = 2 ** 10.5
print('Exponenciação', type(exponencicacao_02), exponencicacao_02)


# MÓDULO 

modulo = 55 % 2.7
print('Módulo', type(modulo), modulo) 

modulo_02 = 50 % 2
print('Módulo', type(modulo_02), modulo_02)

modulo_03 = 55 % 2
print('Módulo', type(modulo_03), modulo_03)

